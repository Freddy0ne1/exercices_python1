"""Exercice 01 : Écrire un programme qui demande à l'utilisateur son nom
et son âge, puis affiche un message de bienvenue.
"""

# Demande du nom et de l'âge de l'utilisateur
name = input("Entrez votre nom : ")
# Demande de l'âge de l'utilisateur
age = int(input("Entrez votre âge : "))
# Affichage du message de bienvenue
print(f"Bonjour, je m'appelle {name} et j'ai {age} ans.")
